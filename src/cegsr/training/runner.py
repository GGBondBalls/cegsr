def run_sft(
    config_path: str,
    distributed_config: dict[str, Any] | None = None,
    timeout: int | None = None,
) -> str:
    """
    Run SFT training via LLaMA-Factory.

    Args:
        config_path: Path to the LLaMA-Factory YAML config (generated by adapter).
        distributed_config: Optional distributed training env vars.
        timeout: Max seconds to wait (None = no limit).

    Returns:
        output_dir from the config (where adapter is saved).
    """
    import yaml
    cfg = yaml.safe_load(Path(config_path).read_text(encoding="utf-8"))
    output_dir = cfg.get("output_dir", "")
    logger.info("SFT training: config=%s output=%s", config_path, output_dir)

    env = _build_env_for_distributed(distributed_config) if distributed_config else None
    _run_llamafactory_cli("train", config_path, env_overrides=env, timeout=timeout)

    logger.info("SFT training complete: %s", output_dir)
    return output_dir


