def merge_lora(
    config_path: str,
) -> str:
    """
    Merge LoRA adapter into base model via LLaMA-Factory export.

    Returns:
        export_dir (path to the merged model).
    """
    import yaml
    cfg = yaml.safe_load(Path(config_path).read_text(encoding="utf-8"))
    export_dir = cfg.get("export_dir", "")
    logger.info("Merging LoRA: config=%s output=%s", config_path, export_dir)

    _run_llamafactory_cli("export", config_path)

    logger.info("LoRA merge complete: %s", export_dir)
    return export_dir


def run_training_pipeline(
    export_dir: str,
    training_mode: str = "lora",
    run_dpo_stage: bool = False,
    merge_after_training: bool = True,
    distributed_config: dict[str, Any] | None = None,
    timeout_per_stage: int | None = None,
) -> dict[str, str]:
    """
    Execute the full training pipeline: SFT → (optional DPO) → (optional merge).

    Args:
        export_dir: Directory with generated LLaMA-Factory configs (from adapter).
        training_mode: "lora" or "qlora".
        run_dpo_stage: Whether to run DPO after SFT.
        merge_after_training: Whether to merge the final adapter into base model.
        distributed_config: Optional distributed training config.
        timeout_per_stage: Max seconds per training stage.

    Returns:
        Dict with paths: {"sft_adapter", "dpo_adapter" (optional), "merged_model" (optional)}.
    """
    export_path = Path(export_dir)
    results: dict[str, str] = {}

    # Stage 1: SFT (combined adapter - all roles)
    sft_config = export_path / f"llamafactory_combined_{training_mode}.yaml"
    if not sft_config.exists():
        # Fallback: find any SFT config
        candidates = sorted(export_path.glob(f"llamafactory_*_{training_mode}.yaml"))
        candidates = [c for c in candidates if "dpo" not in c.name and "merge" not in c.name]
        if not candidates:
            raise TrainingError(f"No SFT config found for mode '{training_mode}' in {export_dir}")
        sft_config = candidates[0]
        logger.warning("Combined config not found, using: %s", sft_config)

    sft_output = run_sft(str(sft_config), distributed_config=distributed_config, timeout=timeout_per_stage)
    results["sft_adapter"] = sft_output

    # Stage 2: DPO (optional)
    if run_dpo_stage:
        dpo_config = export_path / "llamafactory_dpo.yaml"
        if dpo_config.exists():
            dpo_output = run_dpo(str(dpo_config), distributed_config=distributed_config, timeout=timeout_per_stage)
            results["dpo_adapter"] = dpo_output
        else:
            logger.warning("DPO config not found, skipping DPO stage")

    # Stage 3: Merge (optional)
    if merge_after_training:
        merge_config = export_path / "llamafactory_merge.yaml"
        if merge_config.exists():
            merged_path = merge_lora(str(merge_config))
            results["merged_model"] = merged_path
        else:
            logger.warning("Merge config not found, skipping merge")

    # Save results
    write_json(export_path / "training_results.json", results)
    logger.info("Training pipeline complete: %s", results)
    return results
