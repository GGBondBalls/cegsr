"""
Training execution engine.

Calls LLaMA-Factory via subprocess to run SFT, DPO, and merge operations.
Designed for Qwen2.5 + dual RTX 4090 environment.
"""
from __future__ import annotations

import json
import subprocess
import sys
from pathlib import Path
from typing import Any

from cegsr.utils.io import ensure_dir, write_json
from cegsr.utils.logging import get_logger

logger = get_logger(__name__)


class TrainingError(RuntimeError):
    """Raised when a LLaMA-Factory training job fails."""


def _run_llamafactory_cli(
    subcommand: str,
    config_path: str,
    env_overrides: dict[str, str] | None = None,
    timeout: int | None = None,
) -> subprocess.CompletedProcess[str]:
    """Run ``llamafactory-cli <subcommand> <config>`` and stream output."""
    import os
    env = {**os.environ, **(env_overrides or {})}
    cmd = ["llamafactory-cli", subcommand, config_path]
    logger.info("Running: %s", " ".join(cmd))
    result = subprocess.run(
        cmd,
        env=env,
        text=True,
        timeout=timeout,
    )
    if result.returncode != 0:
        raise TrainingError(
            f"llamafactory-cli {subcommand} failed (exit {result.returncode}): {config_path}"
        )
    return result


def _build_env_for_distributed(distributed_config: dict[str, Any]) -> dict[str, str]:
    """Build environment variables for distributed training."""
    env: dict[str, str] = {}
    gpus = distributed_config.get("gpus")
    if gpus:
        if isinstance(gpus, (list, tuple)):
            env["CUDA_VISIBLE_DEVICES"] = ",".join(str(g) for g in gpus)
        else:
            env["CUDA_VISIBLE_DEVICES"] = str(gpus)
    if distributed_config.get("force_torchrun", True):
        env["FORCE_TORCHRUN"] = "1"
    env["NNODES"] = str(distributed_config.get("nnodes", 1))
    env["NODE_RANK"] = str(distributed_config.get("node_rank", 0))
    nproc = distributed_config.get("nproc_per_node")
    if nproc is None and gpus:
        nproc = len(env["CUDA_VISIBLE_DEVICES"].split(","))
    env["NPROC_PER_NODE"] = str(nproc or 1)
    env["MASTER_ADDR"] = str(distributed_config.get("master_addr", "127.0.0.1"))
    env["MASTER_PORT"] = str(distributed_config.get("master_port", 29500))
    return env


